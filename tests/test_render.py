def test_renders_stakeholders_with_workload():
    env = _minimal_envelope()
    env["pm_dashboard"] = {
        "stakeholders": [
            {"slug": "sarah_chen", "name": "Sarah Chen", "role": "Project Sponsor", "email": "schen@acme.com", "phone": ""},
        ],
        "money_summary": {"total": 0, "atoms": []},
    }
    env["stakeholder_load"] = {
        "stakeholder_count": 1,
        "stakeholders": [
            {
                "slug": "sarah_chen",
                "risk_count": 2,
                "critical_risk_count": 1,
                "high_risk_count": 1,
                "action_item_count": 0,
                "risk_severity_load": 8,
            },
        ],
        "bottlenecks": [],
    }
    md = build_sow_markdown(env)
    assert "## 3. Stakeholders" in md
    assert "Sarah Chen" in md
    assert "Project Sponsor" in md
    assert "schen@acme.com" in md
    assert "**Workload allocation:**" in md
    assert "sarah_chen" in md
    assert "| 2 | 1 | 1" in md  # risks=2, critical=1, high=1


def test_renders_risk_register_sorted_by_severity():
    env = _minimal_envelope()
    env["pm_dashboard"] = {
        "stakeholders": [],
        "risks_by_owner": {
            "owner_a": [
                {"risk_id": "R-001", "severity": "Low", "summary": "Low risk", "mitigation": "Watch"},
                {"risk_id": "R-002", "severity": "Critical", "summary": "Critical risk", "mitigation": "Mitigate now"},
                {"risk_id": "R-003", "severity": "High", "summary": "High risk", "mitigation": "Pre-stage"},
            ],
        },
        "risks_unowned": [],
        "money_summary": {"total": 0, "atoms": []},
    }
    md = build_sow_markdown(env)
    assert "## 13. Risk Register" in md
    # Critical should appear before High, High before Low
    crit_pos = md.find("R-002")
    high_pos = md.find("R-003")
    low_pos = md.find("R-001")
    assert crit_pos < high_pos < low_pos, f"risks not sorted by severity (positions: critical={crit_pos}, high={high_pos}, low={low_pos})"


def test_deterministic_output():
    env = _minimal_envelope()
    md1 = build_sow_markdown(env)
    md2 = build_sow_markdown(env)
    assert md1 == md2  # byte-identical on same input
